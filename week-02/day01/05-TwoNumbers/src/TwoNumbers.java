public class TwoNumbers {
    public static void main(String[] args) {
        System.out.println(13 + 22);
        System.out.println(13 - 22);
        System.out.println(22 * 13);
        float reminder = (22f / 13f);
        System.out.println(reminder);
        System.out.println(22 / 13);
        System.out.println(reminder);

    }
}
